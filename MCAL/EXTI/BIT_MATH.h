#ifndef _BIT_MATH_H
#define _BIT_MATH_H


#define SET_BIT(reg,BitNo)    ((reg) |=  (1 << (BitNo)))
#define CLR_BIT(reg,BitNo)    ((reg) &= ~(1 << (BitNo)))
#define TOG_BIT(reg,BitNo)    ((reg) ^=  (1 << (BitNo)))
#define GET_BIT(reg ,BitNo)	 (((reg) >> (BitNo)) & 0x01 )

#endif
